#include <iostream>
using namespace std;

int main()
{
    int x;

    cout << "Inputkan sebuah angka : ";
    cin >> x;

    // inisialisasi, kondisi, iterasi
    for (int a = x; a <= 10; a++) {
        cout << a << endl;
    }

    // proses 1 : a->1 lalu dicek kondisi benar -> tampilkan dulu baru ditambah
}